import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:task_flow/features/auth/bloc/auth_bloc.dart';
import 'package:task_flow/features/task/widgets/create_task_form.dart';
import 'package:task_flow/features/task/widgets/kanban_board.dart';
import 'package:task_flow/shared/models/task.dart';
import 'package:task_flow/shared/services/task_service.dart';
import 'package:task_flow/shared/widgets/bottom_sheet_wrapper.dart';

class KanbanBoardScreen extends StatefulWidget {
  final String workspaceId;
  final String projectId;
  final String projectName;
  final VoidCallback? onTaskCreated;

  const KanbanBoardScreen({
    super.key,
    required this.workspaceId,
    required this.projectId,
    required this.projectName,
    this.onTaskCreated,
  });

  @override
  State<KanbanBoardScreen> createState() => _KanbanBoardScreenState();
}

class _KanbanBoardScreenState extends State<KanbanBoardScreen> {
  late TaskService _taskService;
  List<Task> _tasks = [];
  bool _isLoading = true;

  @override
  void initState() {
    super.initState();
    _taskService = TaskService();
    _loadTasks();
  }

  Future<void> _loadTasks() async {
    try {
      final tasks = await _taskService.getProjectTasks(
        widget.workspaceId,
        widget.projectId,
      );
      if (mounted) {
        setState(() {
          _tasks = tasks;
          _isLoading = false;
        });
      }
    } catch (e) {
      if (mounted) {
        setState(() {
          _isLoading = false;
        });
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('Failed to load tasks: $e'),
            backgroundColor: Colors.red,
          ),
        );
      }
    }
  }

  void _onTaskStatusChanged(Task task, String newStatus) async {
    try {
      await _taskService.updateTaskStatus(
        widget.workspaceId,
        widget.projectId,
        task.id,
        newStatus,
      );

      if (mounted) {
        // Reload tasks to reflect the change
        _loadTasks();

        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(
            content: Text('Task status updated'),
            backgroundColor: Colors.green,
          ),
        );
      }
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('Failed to update task status: $e'),
            backgroundColor: Colors.red,
          ),
        );
      }
    }
  }

  void _showCreateTaskBottomSheet() {
    final user = context.read<AuthBloc>().state.user;
    if (user == null) return;

    showModalBottomSheet(
      context: context,
      isScrollControlled: true,
      builder: (BuildContext bottomSheetContext) {
        return BottomSheetWrapper(
          title: 'Create Task',
          content: SingleChildScrollView(
            child: CreateTaskForm(
              projectId: widget.projectId,
              reporterId: user.uid,
              onCreate: (task) async {
                try {
                  final newTask = await _taskService.createTask(
                    workspaceId: widget.workspaceId,
                    projectId: widget.projectId,
                    title: task.title,
                    description: task.description,
                    status: task.status,
                    assigneeId: task.assigneeId,
                    reporterId: task.reporterId,
                    dueDate: task.dueDate,
                    priority: task.priority,
                  );

                  if (bottomSheetContext.mounted) {
                    setState(() {
                      _tasks.add(newTask);
                    });

                    Navigator.pop(bottomSheetContext); // Close the bottom sheet

                    ScaffoldMessenger.of(bottomSheetContext).showSnackBar(
                      const SnackBar(
                        content: Text('Task created successfully'),
                        backgroundColor: Colors.green,
                      ),
                    );

                    // Reload tasks to ensure consistency
                    _loadTasks();
                    
                    // Notify that a task was created
                    if (widget.onTaskCreated != null) {
                      widget.onTaskCreated!();
                    }
                  }
                } catch (e) {
                  if (bottomSheetContext.mounted) {
                    ScaffoldMessenger.of(bottomSheetContext).showSnackBar(
                      SnackBar(
                        content: Text('Failed to create task: $e'),
                        backgroundColor: Colors.red,
                      ),
                    );
                  }
                }
              },
            ),
          ),
          onCreate: () {
            // The form will handle submission
          },
        );
      },
    );
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text(widget.projectName),
        actions: [
          IconButton(
            onPressed: _showCreateTaskBottomSheet,
            icon: const Icon(Icons.add),
          ),
        ],
      ),
      body: _isLoading
          ? const Center(child: CircularProgressIndicator())
          : KanbanBoard(
              tasks: _tasks,
              onTaskTap: (task) {
                // TODO: Implement task tap
              },
              onTaskEdit: (task) {
                // TODO: Implement task edit
              },
              onTaskDelete: (task) {
                // TODO: Implement task delete
              },
              onTaskStatusChanged: _onTaskStatusChanged,
            ),
      floatingActionButton: FloatingActionButton(
        tooltip: 'Add new task',
        heroTag: 'addTask',
        onPressed: _showCreateTaskBottomSheet,
        child: const Icon(Icons.add),
      ),
    );
  }
}